package com.ztgeo.general.entity.extend;

public class NumberDisplay {

    private String agency;

    private String participationIncompleteNumber;

    private String hangUp;

    public String getAgency() {
        return agency;
    }

    public void setAgency(String agency) {
        this.agency = agency;
    }

    public String getParticipationIncompleteNumber() {
        return participationIncompleteNumber;
    }

    public void setParticipationIncompleteNumber(String participationIncompleteNumber) {
        this.participationIncompleteNumber = participationIncompleteNumber;
    }

    public String getHangUp() {
        return hangUp;
    }

    public void setHangUp(String hangUp) {
        this.hangUp = hangUp;
    }
}
